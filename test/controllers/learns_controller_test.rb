require 'test_helper'

class LearnsControllerTest < ActionDispatch::IntegrationTest
  setup do
    @learn = learns(:one)
  end

  test "should get index" do
    get learns_url
    assert_response :success
  end

  test "should get new" do
    get new_learn_url
    assert_response :success
  end

  test "should create learn" do
    assert_difference('Learn.count') do
      post learns_url, params: { learn: { thumbnail: @learn.thumbnail, thumbnail_size: @learn.thumbnail_size, thumbnail_type: @learn.thumbnail_type, video_link: @learn.video_link } }
    end

    assert_redirected_to learn_url(Learn.last)
  end

  test "should show learn" do
    get learn_url(@learn)
    assert_response :success
  end

  test "should get edit" do
    get edit_learn_url(@learn)
    assert_response :success
  end

  test "should update learn" do
    patch learn_url(@learn), params: { learn: { thumbnail: @learn.thumbnail, thumbnail_size: @learn.thumbnail_size, thumbnail_type: @learn.thumbnail_type, video_link: @learn.video_link } }
    assert_redirected_to learn_url(@learn)
  end

  test "should destroy learn" do
    assert_difference('Learn.count', -1) do
      delete learn_url(@learn)
    end

    assert_redirected_to learns_url
  end
end
